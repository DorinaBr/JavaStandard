package session3.challenges;

//Write a program where you perform operations between an integer and a double. Display the result.
public class ChallengeSeven {
    public static void main(String[] args) {
        int number = 3;
        double myDouble = 2.5;
        String newLine = System.lineSeparator();
        System.out.println((number + myDouble) + newLine + (number - myDouble) + newLine + (number * myDouble) + newLine + (number / myDouble) + newLine + (number % myDouble));
    }
}
